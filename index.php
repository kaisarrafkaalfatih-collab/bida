<h2 class="title" ><a href="#" class="backButton"></a>SEKSI BIMAS ISLAM</h2>

<div class="row">
<div class="col-md-9">
<p>
Puji syukur kami panjatkan ke hadlirat Alloh SWT., atas segala nikmat dan karunia-Nya, sehingga portal data Bimas Islam dalam Angka dapat kami selesaikan. Shalawat dan salam semoga selalu terlimpah kepada Penghulu Alam, Nabi Muhammad SAW., keluarganya, sahabatnya, dan semua umatnya hingga akhir zaman.
</p><p>
Saya, atas nama keluarga besar Seksi Bimas Islam, menyambut baik tersedinya portal data Bimas Islam dalam Angka ini. Kehadiran portal data ini merupakan bagian dari inovasi dan kesungguhan untuk memberikan pelayanan prima kepada masyarakat. Sehingga, masyarakat cukup membuka link website Kementerian Agama Kantor Kota Bandung, lalu berkunjung ke Portal Data Bimas Islam dalam Angka, tanpa harus bersusah payah datang ke kantor.
</p><p>
Kami berharap, semoga ke depan semakin banyak portal-portal serupa, yang membantu memudahkan masyarakat dalam mengkases data. Sehingga, kemudahan, kecepatan, keakuratan, dan ketersebaran informasi mengenai data tersebut bisa berlangsung lebih efektif.
</p><p>
Akhirnya, kami tetap mengaharapkan saran dan masukan yang konstruktif dari semua pihak. Tujuannya untuk melakukan penyempurnaan di masa yang akan datang. Selamat menikmati Bimas Islam dalam Angka.

